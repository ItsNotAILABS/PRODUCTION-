#!/usr/bin/env node
/**
 * 🚀 organism-release-bot — Changelog Generator
 * ═══════════════════════════════════════════════
 *
 * Generates a changelog from git history for the current release.
 * Groups commits by bot identity and type.
 *
 * Usage:
 *   node scripts/generate-changelog.js
 */

'use strict';

const fs   = require('fs');
const path = require('path');
const cp   = require('child_process');

const REPO = path.resolve(__dirname, '..');
const DIST = path.join(REPO, 'dist');

function main() {
  console.log('');
  console.log('🚀 Changelog Generator');
  console.log('═══════════════════════════════════════════');
  console.log('');

  fs.mkdirSync(DIST, { recursive: true });

  let tag = '';
  try {
    tag = cp.execSync('git describe --tags --abbrev=0', { encoding: 'utf8', cwd: REPO }).trim();
  } catch {
    tag = 'HEAD';
  }

  let prevTag = '';
  try {
    prevTag = cp.execSync(`git describe --tags --abbrev=0 ${tag}^`, { encoding: 'utf8', cwd: REPO }).trim();
  } catch {
    prevTag = '';
  }

  const range = prevTag ? `${prevTag}..${tag}` : tag;

  let commits = [];
  try {
    const log = cp.execSync(`git log ${range} --pretty=format:"%H|%an|%s" --no-merges`, {
      encoding: 'utf8',
      cwd: REPO,
    }).trim();
    if (log) {
      commits = log.split('\n').map(line => {
        const [hash, author, message] = line.split('|');
        return { hash: (hash || '').slice(0, 8), author: author || 'unknown', message: message || '' };
      });
    }
  } catch {
    // No commits in range
  }

  // Group by author/bot
  const groups = {};
  for (const c of commits) {
    const group = c.author.includes('bot') ? c.author : 'Contributors';
    if (!groups[group]) groups[group] = [];
    groups[group].push(c);
  }

  const lines = [
    `# Changelog — ${tag || 'Unreleased'}`,
    '',
    `> Generated on ${new Date().toUTCString()}`,
    '',
  ];

  if (commits.length === 0) {
    lines.push('No commits in this release range.');
  } else {
    for (const [group, groupCommits] of Object.entries(groups)) {
      lines.push(`## ${group}`);
      lines.push('');
      for (const c of groupCommits) {
        lines.push(`- ${c.message} (\`${c.hash}\`)`);
      }
      lines.push('');
    }
  }

  lines.push('---');
  lines.push('*Generated by organism-release-bot*');

  fs.writeFileSync(path.join(DIST, 'CHANGELOG.md'), lines.join('\n'));
  console.log(`  📄 Changelog written (${commits.length} commits)`);
  console.log('  ✅ Changelog generation complete');
  console.log('');
}

main();
