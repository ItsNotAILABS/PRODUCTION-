#!/usr/bin/env node
/**
 * 🌐 organism-deploy-bot — Deployment Validator
 * ══════════════════════════════════════════════
 *
 * Validates deployment configuration:
 *  - Checks dfx.json structure and canister definitions
 *  - Validates organism runtime modules exist
 *  - Generates deployment manifest
 *
 * Usage:
 *   node scripts/validate-deployment.js              # validate
 *   node scripts/validate-deployment.js --runtimes   # check runtimes
 *   node scripts/validate-deployment.js --manifest   # generate manifest
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const REPO = path.resolve(__dirname, '..');
const DOCS = path.join(REPO, 'docs');

const doRuntimes = process.argv.includes('--runtimes');
const doManifest = process.argv.includes('--manifest');

function main() {
  console.log('');
  console.log('🌐 Deployment Validator');
  console.log('═══════════════════════════════════════════');
  console.log('');

  let errors = 0;

  // Validate dfx.json
  const dfxPath = path.join(REPO, 'dfx.json');
  if (fs.existsSync(dfxPath)) {
    try {
      const dfx = JSON.parse(fs.readFileSync(dfxPath, 'utf8'));
      const canisters = Object.keys(dfx.canisters || {});
      console.log(`  ✅ dfx.json valid — ${canisters.length} canisters defined`);
      for (const c of canisters) {
        const canister = dfx.canisters[c];
        console.log(`    • ${c} (${canister.type || 'unknown'}) → ${canister.main || canister.source || '?'}`);
      }
    } catch (err) {
      console.log(`  ❌ dfx.json invalid: ${err.message}`);
      errors++;
    }
  } else {
    console.log('  ⚠ No dfx.json found');
  }

  // Check organism runtimes
  if (doRuntimes) {
    console.log('');
    console.log('  Checking organism runtimes...');
    const orgDir = path.join(REPO, 'organism');
    if (fs.existsSync(orgDir)) {
      const runtimes = fs.readdirSync(orgDir, { withFileTypes: true }).filter(d => d.isDirectory());
      for (const rt of runtimes) {
        const rtPath = path.join(orgDir, rt.name);
        const files = fs.readdirSync(rtPath);
        console.log(`    ✅ ${rt.name} — ${files.length} files`);
      }
    } else {
      console.log('    ⚠ No organism/ directory');
    }
  }

  // Generate deployment manifest
  if (doManifest) {
    console.log('');
    console.log('  Generating deployment manifest...');
    fs.mkdirSync(DOCS, { recursive: true });

    const lines = [
      '# 🌐 Deployment Manifest',
      '',
      `> Auto-generated by organism-deploy-bot on ${new Date().toUTCString()}`,
      '',
      '## Deployment Targets',
      '',
      '| Target | Status | URL |',
      '|--------|--------|-----|',
      '| GitHub Pages | Active | See deploy-pages workflow |',
      '| ICP Canister | Configured | See dfx.json |',
      '',
    ];

    // List organism runtimes as deployable modules
    const orgDir = path.join(REPO, 'organism');
    if (fs.existsSync(orgDir)) {
      lines.push('## Organism Runtime Modules');
      lines.push('');
      const runtimes = fs.readdirSync(orgDir, { withFileTypes: true }).filter(d => d.isDirectory());
      for (const rt of runtimes) {
        lines.push(`- **${rt.name}** — \`organism/${rt.name}/\``);
      }
      lines.push('');
    }

    lines.push('---');
    lines.push('*Generated by organism-deploy-bot*');

    fs.writeFileSync(path.join(DOCS, 'deployment-manifest.md'), lines.join('\n'));
    console.log(`  📄 Manifest written to docs/deployment-manifest.md`);
  }

  console.log('');
  console.log('  ✅ Deployment validation complete');
  console.log('');

  if (errors > 0) process.exit(1);
}

main();
