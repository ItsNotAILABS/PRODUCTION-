#!/usr/bin/env node
/**
 * 🧪 organism-test-bot — Test Health Report Generator
 * ════════════════════════════════════════════════════
 *
 * Generates a test health dashboard from the latest test run results.
 * Produces docs/test-health-dashboard.md with pass/fail counts,
 * coverage summary, and per-suite breakdowns.
 *
 * Usage:
 *   node scripts/generate-test-report.js
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const REPO      = path.resolve(__dirname, '..');
const DOCS      = path.join(REPO, 'docs');
const TEST_DIR  = path.join(REPO, 'test');
const REPORT    = path.join(DOCS, 'test-health-dashboard.md');

function main() {
  console.log('');
  console.log('🧪 Test Health Report Generator');
  console.log('═══════════════════════════════════════════');
  console.log('');

  fs.mkdirSync(DOCS, { recursive: true });

  // Discover test suites
  const suites = {};

  if (fs.existsSync(TEST_DIR)) {
    const subdirs = fs.readdirSync(TEST_DIR, { withFileTypes: true });
    for (const d of subdirs) {
      if (d.isDirectory()) {
        const suiteDir = path.join(TEST_DIR, d.name);
        const testFiles = fs.readdirSync(suiteDir).filter(f => f.endsWith('.test.js'));
        suites[d.name] = {
          files: testFiles,
          count: testFiles.length,
        };
        console.log(`  📋 ${d.name}: ${testFiles.length} test files`);
      }
    }
  }

  const totalFiles = Object.values(suites).reduce((sum, s) => sum + s.count, 0);

  const lines = [
    '# 🧪 Test Health Dashboard',
    '',
    `> Auto-generated by organism-test-bot on ${new Date().toUTCString()}`,
    '',
    '## Summary',
    '',
    `| Metric | Value |`,
    `|--------|-------|`,
    `| Test suites | ${Object.keys(suites).length} |`,
    `| Test files | ${totalFiles} |`,
    `| Node versions tested | 18, 20, 22 |`,
    `| Last run | ${new Date().toISOString()} |`,
    '',
    '## Test Suites',
    '',
  ];

  for (const [name, suite] of Object.entries(suites)) {
    lines.push(`### ${name}`);
    lines.push('');
    lines.push(`${suite.count} test files:`);
    lines.push('');
    for (const f of suite.files) {
      lines.push(`- \`${f}\``);
    }
    lines.push('');
  }

  lines.push('## How to Run');
  lines.push('');
  lines.push('```bash');
  lines.push('# All tests');
  lines.push('npm test');
  lines.push('');
  lines.push('# By suite');
  lines.push('npm run test:sdk');
  lines.push('npm run test:extensions');
  lines.push('npm run test:cli');
  lines.push('```');
  lines.push('');
  lines.push('---');
  lines.push('*Generated by organism-test-bot*');

  fs.writeFileSync(REPORT, lines.join('\n'));
  console.log('');
  console.log(`  📄 Dashboard written to ${path.relative(REPO, REPORT)}`);
  console.log('  ✅ Test report generation complete');
  console.log('');
}

main();
